#List work
places_to_visit = ['New Zealand', 'Denmark', 'Iceland', 'Japan', 'Belgium']
print(places_to_visit)

#show sorted
print(sorted(places_to_visit))

#sorted reversed
print(sorted(places_to_visit,reverse = True))

#actually reverse the list
places_to_visit.reverse()
print(places_to_visit)

#reverse again
places_to_visit.reverse()
print(places_to_visit)

#actually sort the list, and change it.
places_to_visit.sort()
print(places_to_visit)
print(len(places_to_visit))
